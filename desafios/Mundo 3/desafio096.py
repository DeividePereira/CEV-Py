# Crie uma função chamada área(), que receba as dimensões de um
# terreno retangular (largura e comprimento); mostre a área do terreno.
def area():
    terreno = comp * larg
    print(f'Área: {terreno:.1f} m²')


print(' Cálculo de Área de Retângulos ')
print('-------------------------------')
comp = float(input('Comprimento (m): '))
larg = float(input('Largura (m): '))
area()
